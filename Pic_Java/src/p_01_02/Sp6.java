package p_01_02;

public class Sp6 {
//10진수 이외의 표기법으로 표기하기
	public static void main(String[] args) {	
		System.out.println("10진수 10은 "+ 10 + "입니다."); 
		System.out.println("8진수 10은 "+ 010 + "입니다.");     //8진수라는 표시 0
		System.out.println("16진수 10은 " + 0x10 + "입니다.");  //16진수라는 표시 0x
		System.out.println("16진수 F는 " + 0xF + "입니다.");
                                   //숫자 리터럴(표시하는 방법)
	}

}
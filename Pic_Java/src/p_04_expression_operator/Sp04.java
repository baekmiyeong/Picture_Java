package p_04_expression_operator;

public class Sp04 {
//다양한 연산자를 사용해 보기
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		
		System.out.println("num1과 num2로 다양한 연산을 수행합니다.");
		System.out.println("num1 + num2는 " + (num1 + num2) + "입니다.");
		System.out.println("num1 - num2는 " + (num1 - num2) + "입니다.");
		System.out.println("num1 * num2는 " + (num1 * num2) + "입니다.");
		System.out.println("num1 / num2는 " + (num1 / num2) + "입니다.");
		System.out.println("num1 % num2는 " + (num1 % num2) + "입니다.");
		                   //피연산자 문자열       //문자열을 연결하는 + 연산자!!
	}

}
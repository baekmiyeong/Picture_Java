package p_03_variable;

public class Sp3 {
//다른 변수에 값을 대입하기
	public static void main(String[] args) {
		int num1, num2;
		num1 = 3; 
				
		System.out.println("변수 num1의 값은 " + num1 + "입니다.");
										
		num2 = num1;
		
		System.out.println("변수의 num1의 값을 변수num2에 대입했습니다.");
		System.out.println("변수 num2의 값은 " + num2 + "입니다.");
	}

}